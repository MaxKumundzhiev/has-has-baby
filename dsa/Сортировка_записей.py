"""
Сортировка записей
Дан список кортежей — записей о студентах (имя, оценка, возраст).
    Отсортируй: сначала по оценке (убывание), при равных — по возрасту (возрастание).

[("Ali",90,20),("Sam",90,19),("Tom",85,22)] → [("Sam",90,19),("Ali",90,20),("Tom",85,22)]

Используй key= в sorted(). Как задать разные направления?
"""


def sort_records(data: list[tuple]) -> list[tuple]:
    return sorted(data, key=lambda x: (-x[1], x[2]))
